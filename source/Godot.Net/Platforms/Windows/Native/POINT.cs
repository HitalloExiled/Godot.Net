namespace Godot.Net.Platforms.Windows.Native;

/// <summary>
/// The POINT structure defines the x- and y- coordinates of a point.
/// </summary>
public struct POINT
{
    /// <summary>
    ///  The x-coordinate of the point.
    /// </summary>
    public int x;

    /// <summary>
    /// The x-coordinate of the point.
    /// </summary>
    public int y;
}
